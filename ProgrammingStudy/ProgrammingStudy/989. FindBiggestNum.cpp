#include <iostream>

using namespace std;


int main()
{
	srand(time(NULL)); // 랜덤 시드 초기화

	int arr[100];
	for (int& i : arr)
	{
		int n = rand() % 100 + 1; // 1~100 사이의 랜덤 숫자 생성
		arr[i] = n; // 배열에 랜덤 숫자 저장
	}
	// 배열의 최대값 찾기
	int maxNum = arr[0];
	for (int i : arr)
	{
		if (i > maxNum)
		{
			maxNum = i; // 최대값 갱신
		}
	}


	return 0;
}